MOV TMOD, #50H ; COUNTER IN MODE 1
SETB TR1 ; START COUNTER

MOV DPTR, #DATA

SETB P3.0
CLR P3.1 ; FORWARD ROTATION OF MOTOR
LOOP:
MOV A, TL1 ; MOVE COUNT IN A
CJNE A, #0AH, DISP ; JUMP TO DISP IF COUNT IS LESS THAN 9
MOV TL1, #00H ; RESTART THE COUNT
CLR A

DISP:
MOVC A, @A+DPTR ; DISPLAY THE COUNT ON SEVEN SEGMENT
MOV P1, A
JMP LOOP

; DATA OF LEDS FOR SEVEN SEGMENT DISPLAY
DATA:
DB 11000000B, 11111001B, 10100100B, 10110000B, 10011001B, 10010010B, 10000010B, 11111000B, 10000000B, 10010000B
